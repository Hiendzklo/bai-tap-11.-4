// Khai báo 3 biến num1, num2, num3 với kiểu dữ liệu number
let num1: number = 10;
let num2: number = 5;
let num3: number;

// Thực hiện các phép tính cộng, trừ, nhân, chia và lưu kết quả vào biến num3
num3 = num1 + num2;
console.log("Result of addition:", num3);

num3 = num1 - num2;
console.log("Result of subtraction:", num3);

num3 = num1 * num2;
console.log("Result of multiplication:", num3);

num3 = num1 / num2;
console.log("Result of division:", num3);

