// Khai báo 3 biến với kiểu dữ liệu number
let num1_1: number;
let num2_1: number;
let result_1: number;

// Gán giá trị cho hai biến
num1_1 = 10;
num2_1 = 5;

// Thực hiện các phép tính toán cơ bản với hai biến này và gán kết quả cho biến thứ 3
result_1 = num1_1 + num2_1;
console.log("Result of addition:", result_1);

result_1 = num1_1 - num2_1;
console.log("Result of subtraction:", result_1);

result_1 = num1_1 * num2_1;
console.log("Result of multiplication:", result_1);

result_1 = num1_1 / num2_1;
console.log("Result of division:", result_1);
