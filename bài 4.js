"use strict";
// Khai báo 3 biến với kiểu dữ liệu number
let num1_2;
let num2_2;
let result_2;
// Gán giá trị cho hai biến
num1_2 = 20;
num2_2 = 15;
// Thực hiện các phép tính toán cơ bản với hai biến này và gán kết quả cho biến thứ 3
result_2 = num1_2 + num2_2;
console.log("Result of addition:", result_2);
result_2 = num1_2 - num2_2;
console.log("Result of subtraction:", result_2);
result_2 = num1_2 * num2_2;
console.log("Result of multiplication:", result_2);
result_2 = num1_2 / num2_2;
console.log("Result of division:", result_2);
